class Operators {
  public static void main(String [] args) {
    int number1 = 20;
    int number2 = 20;
    int number3 = 20;
    int number4 = 20;
    System.out.println("number++: " + number1++); // incrementa na segunda vez
    System.out.println("number-- " + number2--); // decrementa na segunda vez
    System.out.println("++number: " + ++number3); // incrementa antes de imprimir
    System.out.println("--number " + --number4); // decrementa antes de imprimir
  }
}
